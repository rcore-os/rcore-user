void init()
{
	glEnable(GL_DEPTH_TEST);
	glDepthFunc(GL_LESS);
	glClearColor(0.64f, 0.81f, 0.38f, 1.0f);
	glClearDepthf(1);
}

void draw(float angle)
{
	glClear(GL_COLOR_BUFFER_BIT | GL_DEPTH_BUFFER_BIT);

	glVertexPointer(3, GL_FLOAT, 0, vertexs);
	glColorPointer(4, GL_FLOAT, 0, colors);
	glEnableClientState(GL_VERTEX_ARRAY);
	glEnableClientState(GL_COLOR_ARRAY);

	int len = sizeof(vertexs) / ( 3 * sizeof(GLfloat) );

	{
		glViewport(0, 0, 960, 540);

		glLoadIdentity();
		glRotatef(angle, 0.0, 1.0, 0.0);
		glRotatef(30.0, 1.0, 0.0, 0.0);

		glColor4f(1, 1, 1, 1);
		glDrawArrays(GL_TRIANGLES, 0, len);
	}

	glDisableClientState(GL_VERTEX_ARRAY);
	glDisableClientState(GL_COLOR_ARRAY);

	glFlush();
	eglSwapBuffers(dpy);
}

int main(int argc, char *argv[])
{
	printf("Test porygon testing...\n");

	dpy = eglGetDisplay(EGL_DEFAULT_DISPLAY);
	ctx = eglCreateContext(dpy);

	if (!eglMakeCurrent(dpy, ctx)) {
		return 1;
	}

	init();

	struct timeval tv_begin, tv_end;
	struct timezone tz;

	gettimeofday(&tv_begin, &tz);

	float delta = 0.1;
	float angle = 0.0;
	while (angle <= 360.0) {
		draw(angle);
		angle += delta;
	}
	gettimeofday(&tv_end, &tz);
	
	eglDestroyContext(dpy, ctx);

	float times = 1.0 * (tv_end.tv_sec - tv_begin.tv_sec) + 1.0 * (tv_end.tv_usec - tv_begin.tv_usec) / 1000000.0;

	printf("whole time: %f s\n", times);
	printf("frequency: %f\n", 3600.0 / times);

	return 0;
}